package com.mukund.tutorials.dto;

public class TutorialsDTO {
private String courseName;
private String courseDuration;
private Integer courseFee;
private String tutorName;
private String courseDomain;

public String getCourseName() {
	return courseName;
}
public void setCourseName(String courseName) {
	this.courseName = courseName;
}
public String getCourseDuration() {
	return courseDuration;
}
public void setCourseDuration(String courseDuration) {
	this.courseDuration = courseDuration;
}
public Integer getCourseFee() {
	return courseFee;
}
public void setCourseFee(Integer courseFee) {
	this.courseFee = courseFee;
}
public String getTutorName() {
	return tutorName;
}
public void setTutorName(String tutorName) {
	this.tutorName = tutorName;
}
public String getCourseDomain() {
	return courseDomain;
}
public void setCourseDomain(String courseDomain) {
	this.courseDomain = courseDomain;
}


}
